<h2>7. Securitatea datelor</h2>
<ul>
	<li>Comunicare criptată HTTPS/TLS pe toate aplicațiile</li>
	<li>Token-uri de autentificare în cookie-uri httpOnly (JS-ul nu accesează token-ul)</li>
	<li>Acces pe bază de roluri (Role-Based Access Control)</li>
	<li>Backup criptat zilnic</li>
	<li>Pseudonimizare unde e posibil</li>
	<li>Personal instruit periodic pe GDPR</li>
	<li>Procedură de notificare a breșelor către ANSPDCP în <strong>72 de ore</strong> (art. 33 GDPR)</li>
</ul>

<h2>8. Decizii automate și profilare</h2>
<p>
	Nu luăm decizii bazate exclusiv pe prelucrare automată cu efecte juridice asupra ta. Folosim algoritmi
	pentru a-ți recomanda revizii, dar decizia finală despre orice intervenție o iei tu împreună cu consilierul.
</p>

