<h2>4. Cui transmitem datele tale</h2>
<ul>
	<li><strong>Furnizori de piese</strong> — pentru comandă piese (date despre vehicul + nume)</li>
	<li><strong>RAR / Registrul Auto Român</strong> — la ITP, dacă e cazul</li>
	<li><strong>Asigurători</strong> — pentru daune, dacă tu ne autorizezi</li>
	<li><strong>Furnizori IT</strong> — hosting (Laravel Cloud), email/SMS gateway, contabilitate cloud — toți au contracte de prelucrare (DPA) conform art. 28 GDPR</li>
	<li><strong>Contabil / auditor</strong> — pentru documente fiscale</li>
	<li><strong>Autorități publice</strong> — ANAF, ANPC, Poliție, instanțe — doar la cerere oficială</li>
	<li><strong>Avocați</strong> — pentru recuperare creanțe sau apărare</li>
</ul>
<p>Nu vindem datele tale și nu le transmitem în afara Spațiului Economic European fără măsuri adecvate (clauze contractuale standard SCC 2021).</p>

<h2>5. Cât timp păstrăm datele</h2>
<ul>
	<li><strong>Date contractuale și de service:</strong> durata relației + 3 ani (termen prescripție)</li>
	<li><strong>Documente fiscale (facturi):</strong> 10 ani (obligație legală)</li>
	<li><strong>Date marketing:</strong> până retragi consimțământul</li>
	<li><strong>Înregistrări CCTV:</strong> 30 de zile</li>
	<li><strong>Log-uri tehnice / acces aplicație:</strong> 12 luni</li>
	<li><strong>Cont portal inactiv:</strong> 3 ani fără activitate → ștergere automată după notificare prealabilă</li>
</ul>

<h2>6. Drepturile tale</h2>
<p>Conform GDPR, ai următoarele drepturi pe care le poți exercita gratuit:</p>
<ul>
	<li><strong>Dreptul de acces</strong> (art. 15) — să afli ce date avem despre tine</li>
	<li><strong>Dreptul la rectificare</strong> (art. 16) — să corectăm datele greșite</li>
	<li><strong>Dreptul la ștergere</strong> / „de a fi uitat" (art. 17) — în limitele permise de lege</li>
	<li><strong>Dreptul la restricționarea prelucrării</strong> (art. 18)</li>
	<li><strong>Dreptul la portabilitate</strong> (art. 20) — să primești datele într-un format citibil de mașină</li>
	<li><strong>Dreptul de opoziție</strong> (art. 21) — în special pentru marketing direct</li>
	<li><strong>Dreptul de a nu fi supus unei decizii automate</strong> (art. 22)</li>
	<li><strong>Dreptul de a-ți retrage consimțământul</strong> oricând (art. 7)</li>
	<li><strong>Dreptul de a depune plângere</strong> la <strong>ANSPDCP</strong> — B-dul G-ral. Gheorghe Magheru 28-30, București, <a href="https://www.dataprotection.ro">dataprotection.ro</a></li>
</ul>

