<h1>Drepturile tale GDPR</h1>
<p class="meta">Conform Regulamentului (UE) 2016/679 — gratuit, în max. 30 de zile</p>

<div class="box">
	Ai opt drepturi fundamentale privind datele tale personale. Le poți exercita oricând —
	gratuit — și îți răspundem în maximum 30 de zile.
</div>

<h2>1. Dreptul de acces (art. 15)</h2>
<p>Ai dreptul să afli exact ce date avem despre tine, în ce scop le folosim și cui le transmitem.</p>
<p>👉 <a href="/dashboard/profil">Profil → Descarcă datele mele</a> — primești un fișier JSON cu tot ce avem.</p>

<h2>2. Dreptul la rectificare (art. 16)</h2>
<p>Dacă o informație este greșită sau incompletă, o corectăm.</p>
<p>👉 <a href="/dashboard/profil">Profil → Solicită corectarea datelor</a></p>

<h2>3. Dreptul la ștergere — „dreptul de a fi uitat" (art. 17)</h2>
<p>
	Poți cere ștergerea datelor tale când:
</p>
<ul>
	<li>nu mai sunt necesare scopului pentru care au fost colectate</li>
	<li>ți-ai retras consimțământul</li>
	<li>te opui prelucrării și nu există motiv legitim</li>
	<li>au fost prelucrate ilegal</li>
</ul>
<p><strong>Limită:</strong> facturile fiscale trebuie păstrate 10 ani conform legii — acestea rămân arhivate chiar dacă ștergi contul.</p>
<p>👉 <a href="/dashboard/profil">Profil → Șterge contul meu</a></p>

<h2>4. Dreptul la restricționarea prelucrării (art. 18)</h2>
<p>Poți cere să „înghețăm" prelucrarea datelor tale în anumite situații (ex: contești corectitudinea lor).</p>
<p>👉 Trimite email la <a href="mailto:gdpr@nlg.ro">gdpr@nlg.ro</a></p>

<h2>5. Dreptul la portabilitate (art. 20)</h2>
<p>
	Primești datele tale într-un format structurat (JSON), pe care îl poți folosi cum dorești —
	inclusiv să le transferi la alt furnizor.
</p>
<p>👉 <a href="/dashboard/profil">Profil → Descarcă datele mele</a></p>

<h2>6. Dreptul de opoziție (art. 21)</h2>
<p>
	Te poți opune oricând prelucrării datelor pe bază de interes legitim sau pentru marketing direct.
	În cazul marketingului, opoziția este absolută — o respectăm imediat.
</p>
<p>👉 <a href="/dashboard/profil">Profil → Toggle „Oferte și promoții"</a></p>

<h2>7. Dreptul de a nu fi supus unei decizii automate (art. 22)</h2>
<p>
	Nu luăm decizii bazate exclusiv pe prelucrare automată cu efecte juridice asupra ta.
	Algoritmii noștri sunt informativi (ex: recomandări revizii), dar deciziile finale sunt luate de oameni.
</p>

<h2>8. Dreptul de a-ți retrage consimțământul (art. 7)</h2>
<p>
	Dacă ai dat consimțământ pentru ceva (ex: marketing, foto), îl poți retrage oricând la fel de ușor
	cum l-ai dat. Retragerea nu afectează prelucrările anterioare.
</p>
<p>👉 <a href="/dashboard/profil">Profil → Confidențialitate</a></p>

